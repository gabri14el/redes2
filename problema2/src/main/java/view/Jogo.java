    private void configura() {
        lista_jogadores.setDragEnabled(false);
        lista_palavras.setDragEnabled(false);

        lista_palavras.setModel(new DefaultListModel<String>());
        lista_jogadores.setModel(new DefaultListModel<String>());

        DefaultListModel modelo_jogadores = (DefaultListModel)lista_jogadores.getModel();
        for(String s:jogo.pegaJogadores()){
            modelo_jogadores.addElement(s);
        }

        StringBuilder builder = new StringBuilder();
        String[][] dados = jogo.getDados();

        for(int i=0; i<4; i++){
            for(int j=0; j<4; j++){
                builder.append("    "+dados[i][j]);
                letras.add(dados[i][j]);
            }
            builder.append("\n");
        }


        this.dados.setEditable(false);
        this.dados.setText(builder.toString());
    }

