    public void inicializaContador(){
        new Thread(new Runnable() {
            public void run() {
                int _timer = 120;
                try {
                    Thread.sleep(200);
                } catch (InterruptedException ex) {
                    Logger.getLogger(Jogo.class.getName()).log(Level.SEVERE, null, ex);
                }
                while(_timer > 0){
                    timer.setText(""+_timer--);
                    try {
                        Thread.sleep(1000);
                    } catch (InterruptedException ex) {
                        Logger.getLogger(Jogo.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
                jogo.finalizarJogo();
            }
        }).start();
    }

